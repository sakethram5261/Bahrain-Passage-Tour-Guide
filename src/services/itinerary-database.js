export function compileLocalItinerary(selectedMoods, tier, duration, pace) {
  // Ensure we have at least one valid mood
  const moods = selectedMoods && selectedMoods.length > 0 ? selectedMoods : ['culture']
  
  // Collect candidate spots from spotsCatalog categories matching moods
  // For simplicity, we filter matching spots. If none, we fallback to all spots.
  // spotsCatalog has: qal-at-al-bahrain (empires), muharraq-souq (spice), pearling-path (sea), block-338 (lights),
  // jarada-island (sea), tree-of-life (empires), haji-cafe (spice), aali-pottery (empires), arad-fort (empires),
  // national-museum (sea), al-dar-islands (sea), reef-island (lights), riffa-fort (empires), barbar-temple (empires),
  // al-jasra-house (empires), khalaf-house (sea), manama-souq (spice), al-areen (desert)
  
  const spotMoodMap = {
    'qal-at-al-bahrain': 'empires',
    'muharraq-souq': 'spice',
    'pearling-path': 'sea',
    'block-338': 'lights',
    'jarada-island': 'sea',
    'tree-of-life': 'empires',
    'haji-cafe': 'spice',
    'aali-pottery': 'empires',
    'arad-fort': 'empires',
    'national-museum': 'sea',
    'al-dar-islands': 'sea',
    'reef-island': 'lights',
    'riffa-fort': 'empires',
    'barbar-temple': 'empires',
    'al-jasra-house': 'empires',
    'khalaf-house': 'sea',
    'manama-souq': 'spice',
    'al-areen': 'desert'
  }

  const allSpotIds = Object.keys(spotStories)
  
  // Filter spots that match selected moods
  let selectedSpotIds = allSpotIds.filter(id => {
    const spotMood = spotMoodMap[id] || 'culture'
    return moods.includes(spotMood)
  })

  // If we don't have enough spots, backfill with others
  if (selectedSpotIds.length < duration) {
    const remaining = allSpotIds.filter(id => !selectedSpotIds.includes(id))
    selectedSpotIds = [...selectedSpotIds, ...remaining]
  }

  // Slice to fit a reasonable number of spots based on duration and pace
  // Pace: Relaxed (2 spots/day), Balanced (4 spots/day), Packed (6 spots/day)
  let spotsPerDay = 4
  if (pace === 'Relaxed') spotsPerDay = 2
  if (pace === 'Packed') spotsPerDay = 6
  
  const totalNeeded = duration * spotsPerDay
  let finalSpots = selectedSpotIds.slice(0, totalNeeded)
  
  // If still not enough, loop spots
  while (finalSpots.length < totalNeeded) {
    finalSpots = [...finalSpots, ...selectedSpotIds]
  }
  
  finalSpots = finalSpots.slice(0, totalNeeded)

  // Construct itinerary items
  const itinerary = finalSpots.map((id, index) => {
    const day = Math.floor(index / spotsPerDay) + 1
    
    // Choose appropriate guide description and cost based on tier
    // We will let useItinerary map these details from spotsCatalog,
    // so we only need to return the id and day.
    return {
      id,
      day
    }
  })

  // Build day summaries
  const daySummaries = {}
  for (let d = 1; d <= duration; d++) {
    // Select a theme based on the dominant mood of spots in that day, or random chosen mood
    const currentDaySpots = itinerary.filter(item => item.day === d)
    const dayMoods = currentDaySpots.map(item => spotMoodMap[item.id] || 'culture')
    const primaryMood = dayMoods[0] || moods[d % moods.length]
    
    const themesList = dayThemes[primaryMood] || dayThemes.culture
    const themeTemplate = themesList[d % themesList.length]
    
    daySummaries[d] = `Day ${d}: ${themeTemplate}.`
  }

  return {
    itinerary,
    daySummaries
  }
}
